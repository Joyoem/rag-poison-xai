import torch
import torch.nn.functional as F
from tqdm import tqdm

def run_logit_lens(question, context, model, tokenizer):
    """
    分析模型在特定上下文下，每一层最想输出的单词
    """
    # 构造输入
    prompt = f"Context: {context}\nQuestion: {question}\nAnswer:"
    inputs = tokenizer(prompt, return_tensors="pt").to("cuda")

    # 1. 运行推理并截获所有隐藏状态
    with torch.no_grad():
        outputs = model(**inputs, output_hidden_states=True)
        # hidden_states 是一个元组，包含了从 Embedding 层到第 28 层的所有张量
        all_hidden_states = outputs.hidden_states

    # 2. 获取输出层工具
    lm_head = model.get_output_embeddings()
    final_norm = model.model.norm

    layer_decoded = []

    # 3. 逐层透视
    for i, h_state in enumerate(all_hidden_states):
        # 映射逻辑：Hidden State -> Norm -> LM_Head -> Softmax
        h_norm = final_norm(h_state)
        logits = lm_head(h_norm)

        # 我们只看最后一个 token (模型准备预测的那个词)
        last_token_logits = logits[0, -1, :]
        probs = F.softmax(last_token_logits, dim=-1)

        # 拿到概率最大的 Top-1 单词
        top_token_id = torch.argmax(probs).item()
        word = tokenizer.decode([top_token_id]).strip()
        layer_decoded.append(word)

    return layer_decoded

